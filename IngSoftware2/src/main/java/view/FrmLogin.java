    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        controller.UserController userController = new controller.UserController();
        String email = txtEmail.getText();
        String password = new String(txtPassword.getPassword());

        models.UserModel user = userController.getUser(email);

        if(user != null) {
            Utilities.Encryptor enc = new Utilities.Encryptor();
            if(enc.checkHash(password, user.getPassword())){
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Bienvenido " + user.getNames() + " (" + user.getRole().getName() + ")");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Contrase√±a incorrecta");
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Usuario no encontrado");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnLoginActionPerformed

